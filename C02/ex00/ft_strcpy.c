#include <stdio.h>

char	*ft_strcpy(char *dst, char *src)
{
	int	c;

	c = 0;
	while (src[c] != '\0')
	{
		dst[c] = src[c];
		c++;
	}
	dst[c] = '\0';
	return (dst);
}
