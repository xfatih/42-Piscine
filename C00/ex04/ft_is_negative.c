#include <unistd.h>

void	ft_is_negative(int s)
{
	char	p;
	char	n;

	p = 'P';
	n = 'N';
	if (s < 0)
	{
		write(1, &n, 1);
	}
	else
	{
		write(1, &p, 1);
	}
}
